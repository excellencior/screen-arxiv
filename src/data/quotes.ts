export function getDailyQuote(): string {
  // Use the current date as a seed for deterministic daily rotation
  const today = new Date();
  const dayIndex = Math.floor(today.getTime() / (1000 * 60 * 60 * 24));
  return MINDFUL_QUOTES[dayIndex % MINDFUL_QUOTES.length];
}
